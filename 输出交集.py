def intersection_of_texts(text1, text2):
    # 将每个文本的每一行分割为一个列表
    lines1 = text1.strip().split('\n')
    lines2 = text2.strip().split('\n')
    
    # 使用集合的交集功能来找到两个文本中都存在的行
    intersection = set(lines1) & set(lines2)
    
    return '\n'.join(intersection)

